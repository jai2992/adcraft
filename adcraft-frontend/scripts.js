function populateOptions(selectElement, data) {
  for (var i = 0; i < data.length; i++) {
      var option = document.createElement("option");
      option.value = data[i];
      option.text = data[i];
      selectElement.add(option);
  }
}

// Function to update the slogans dropdown based on the selected category
function updateSlogans() {
  var categorySelect = document.getElementById("category");
  var selectedCategory = categorySelect.value;
  var sloganSelect = document.getElementById("slogan");
  var slogans = jsonData[selectedCategory];

  // Clear existing options
  sloganSelect.innerHTML = "";

  // Populate options for the Slogan dropdown
  populateOptions(sloganSelect, slogans);
}

